class SCLTranslator:
    def __init__(self):
        # В будущем это будет тянуться из L1 (Postgres)
        self._shadow_map = {
            "L_MARK_01": "Стратегия маркетингового доминирования",
            "L_SEO_CORE": "Алгоритм глубокой индексации и ранжирования",
            "S_SAAS_AUTH": "Протокол беспарольной авторизации и подписок",
            "V_GAME_AUDIT": "Система предиктивного анализа игровых активов"
        }

    def to_concept(self, internal_id):
        """Превращает внутренний ID в человеческий смысл для модели."""
        return self._shadow_map.get(internal_id, "Конфиденциальный модуль ядра")

    def sanitize(self, text):
        """Удаляет любые случайные упоминания лигатур из ответа (на всякий случай)."""
        import re
        # Маскируем любые паттерны типа L_..., S_..., V_...
        return re.sub(r'[LSV]_[A-Z0-9_]+', "[ДАННЫЕ ЗАСЕКРЕЧЕНЫ]", text)

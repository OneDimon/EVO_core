-- Миграция 006: HNSW индекс вместо ivfflat + подготовка к нагрузке тысяч пользователей
-- Связана с: db/pg_client.py (hnsw.ef_search в init коннекта), core/librarian.py
--
-- Почему замена ivfflat -> HNSW:
--   ivfflat lists=100 — статичный параметр, подобранный под правило
--   lists ≈ sqrt(rows). При росте базы до миллионов символов (см. расчёт
--   ёмкости шарда в AI_ONBOARDING.md) это значение НЕ пересчитывается
--   автоматически — поиск деградирует до избыточно крупных кластеров.
--   HNSW не требует подбора lists под размер таблицы, быстрее на чтение
--   при сравнимой точности, единственная плата — дольше строится индекс
--   при массовой первичной загрузке (не критично: символы пишутся по
--   одному в реальном времени, не батчами).

-- Требует pgvector >= 0.5.0 (образ pgvector/pgvector:pg16 уже содержит нужную версию).

-- fix: имя индекса в 001_init.sql — scl_vector_idx, не scl_symbols_vector_idx.
-- Ошибка привела к тому, что DROP тихо не срабатывал (IF EXISTS проглатывал
-- ошибку) и оба индекса (ivfflat + hnsw) сосуществовали, дублируя расходы
-- на поддержание индекса при каждой записи.
DROP INDEX IF EXISTS scl_vector_idx;

CREATE INDEX IF NOT EXISTS scl_symbols_vector_hnsw_idx
    ON scl_symbols USING hnsw (vector vector_cosine_ops)
    WITH (m = 16, ef_construction = 64);
-- m=16: количество связей на узел графа — стандартное значение pgvector docs
--       для корпусов до нескольких десятков миллионов записей.
-- ef_construction=64: качество построения индекса при вставке (баланс
--       скорость постройки / точность поиска). Годится без пересчёта
--       для дальнейшего роста базы — в отличие от ivfflat lists.

-- Runtime-параметр качества поиска (recall vs скорость) выставляется на
-- уровне соединения через init-хук в db/pg_client.py::get_pool (hnsw.ef_search),
-- не здесь — ALTER INDEX не поддерживает runtime-параметры HNSW в pgvector.
